#!/usr/bin/env node
/**
 * 从 .env 按白名单生成 packages/gateway/.dev.vars(本地 wrangler dev 的运行时变量)。
 *
 * 只写脚本产物,不手工维护两份(.env.example 头注)。白名单 = Worker 运行时真正需要的变量;
 * CLOUDFLARE_*(部署凭据,走 wrangler OAuth)与纯 CLI/测试变量不进 .dev.vars。
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'
import { parseEnv } from 'node:util'

// Worker 运行时白名单(TB_SECRET_ENCRYPTION_KEY 为 SecretStore 主密钥,env-only)。
const RUNTIME_WHITELIST = [
  'TB_NAME_PREFIX',
  'TB_SECRET_ENCRYPTION_KEY',
  'TB_R2_ACCESS_KEY_ID',
  'TB_R2_SECRET_ACCESS_KEY',
]

const root = join(import.meta.dirname, '..')
const envPath = join(root, '.env')
const outPath = join(root, 'packages', 'gateway', '.dev.vars')

let parsed
try {
  parsed = parseEnv(readFileSync(envPath, 'utf8'))
} catch (err) {
  console.error(`error: cannot read ${envPath}: ${err.message}`)
  console.error('hint: copy .env.example to .env and fill values first.')
  process.exit(1)
}

const lines = []
for (const key of RUNTIME_WHITELIST) {
  const value = parsed[key]
  if (value !== undefined && value !== '') {
    // 双引号包裹并转义 \ 与 " 与换行,避免值含 #/空格/引号/换行时被 dotenv 截断。
    const escaped = value.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n')
    lines.push(`${key}="${escaped}"`)
  }
}

const header = '# AUTO-GENERATED by scripts/gen-dev-vars.mjs from .env — do not edit by hand.\n'
writeFileSync(outPath, `${header}${lines.join('\n')}\n`)
console.log(
  `wrote ${outPath} (${lines.length} vars: ${lines.map((l) => l.split('=')[0]).join(', ')})`,
)
